const modal = document.querySelector("#project-modal");
const modalImages = document.querySelector("#modal-images");
const modalTitle = document.querySelector("#modal-title");
const modalPeriod = document.querySelector("#modal-period");
const modalDescription = document.querySelector("#modal-description");
const modalLink = document.querySelector("#modal-link");
const introSection = document.querySelector(".intro");
const gallerySection = document.querySelector("#works");
const tabButtons = document.querySelectorAll("[data-tab]");
const tabPanels = document.querySelectorAll("[data-tab-panel]");
let lastFocusedElement = null;
let isSectionScrolling = false;
let openingCard = null;

function openProject(projectId) {
  const project = projects[projectId];

  if (!project) {
    return;
  }

  lastFocusedElement = document.activeElement;
  modalTitle.textContent = project.title;
  modalPeriod.textContent = project.period;
  modalDescription.textContent = project.description;
  modalLink.href = project.link;
  modalLink.textContent = "외부페이지";
  modalImages.innerHTML = project.images
    .map((image, index) => `<img src="${image}" alt="${project.title} 프로젝트 이미지 ${index + 1}" />`)
    .join("");

  modal.classList.add("is-open");
  modal.setAttribute("aria-hidden", "false");
  document.body.classList.add("modal-open");
  modal.querySelector(".project-modal__close").focus();
}

function closeProject() {
  modal.classList.remove("is-open");
  modal.setAttribute("aria-hidden", "true");
  document.body.classList.remove("modal-open");

  if (lastFocusedElement) {
    lastFocusedElement.focus();
  }
}

function openProjectWithCardMotion(card) {
  if (openingCard || modal.classList.contains("is-open")) {
    return;
  }

  openingCard = card;
  card.classList.add("is-opening");

  window.setTimeout(() => {
    openProject(card.dataset.project);
    card.classList.remove("is-opening");
    openingCard = null;
  }, 960);
}

document.querySelectorAll("[data-project]").forEach((card) => {
  card.addEventListener("click", () => {
    openProjectWithCardMotion(card);
  });
});

document.querySelectorAll("[data-close-modal]").forEach((button) => {
  button.addEventListener("click", closeProject);
});

tabButtons.forEach((button) => {
  button.addEventListener("click", () => {
    const selectedTab = button.dataset.tab;

    tabButtons.forEach((tabButton) => {
      tabButton.classList.toggle("is-active", tabButton === button);
    });

    tabPanels.forEach((panel) => {
      const isSelected = panel.dataset.tabPanel === selectedTab;
      panel.hidden = !isSelected;
      panel.classList.toggle("is-active", isSelected);
    });
  });
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape" && modal.classList.contains("is-open")) {
    closeProject();
  }
});

introSection.addEventListener(
  "wheel",
  (event) => {
    if (event.deltaY <= 0 || isSectionScrolling || modal.classList.contains("is-open")) {
      return;
    }

    event.preventDefault();
    isSectionScrolling = true;
    gallerySection.scrollIntoView({ behavior: "smooth", block: "start" });

    window.setTimeout(() => {
      isSectionScrolling = false;
    }, 900);
  },
  { passive: false },
);
